public class ExplicitCast {
    public static void main(String s[])
    {
        int i = 100;
        byte b = (byte)i;

        int x = 97;
        char ch = (char)x;
        System.out.println(ch);

        int y = 150;
        byte z = (byte) y;
        System.out.println(z);

        int $c = 20;
        int _p = 60;
        System.out.println($c);
        System.out.println(_p);

        double ab = 20.20;
        float zp = (float) ab;
        System.out.println(zp);


    }
}
