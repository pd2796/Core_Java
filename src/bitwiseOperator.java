public class bitwiseOperator {
    public static void main(String aer[])
    {
        System.out.println(true & true);
        System.out.println(false | false);
        System.out.println(true ^ false);
        System.out.println(4&5);
        System.out.println(4|5);
        System.out.println(4^10);

    }
}
