public class AssignmentOperator {

    public static void main(String at[])
    {
        int x,y,z;

        x = y = z = 2000;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);

        int b = 20;
        b+=40;   // b = b+40;
        System.out.println(b);



    }

}

